# figure out which day of the week it is

date = int(input("What day of the week is it?"))

if date == 1:
    print("Monday")
elif date == 2:
    print("Tuesday")
elif date == 3:
    print("Wednesday")
elif date == 4:
    print("Thursday")
elif date == 5:
    print("Friday")
elif date == 6:
    print("Saturday")
elif date == 7:
    print("Sunday")
else:
    print("incorrect value")
